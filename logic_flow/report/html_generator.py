MAJOR_FUNCTION_NAMES = {
    0: "IRP_MJ_CREATE",
    1: "IRP_MJ_CREATE_NAMED_PIPE",
    2: "IRP_MJ_CLOSE",
    3: "IRP_MJ_READ",
    4: "IRP_MJ_WRITE",
    9: "IRP_MJ_FLUSH_BUFFERS",
    14: "IRP_MJ_DEVICE_CONTROL",
    15: "IRP_MJ_INTERNAL_DEVICE_CONTROL",
    16: "IRP_MJ_SHUTDOWN",
    18: "IRP_MJ_CLEANUP",
    23: "IRP_MJ_PNP",
    22: "IRP_MJ_POWER",
}


class HTMLReportGenerator:
    """Generates HTML reports from analysis data."""
    
    def __init__(self):
        self.interface_data: Dict[str, Any] = {}
        self.triage_data: Dict[str, Any] = {}
    
    def load_interface(self, path_or_data):
        """Load interface data from file or dict."""
        if isinstance(path_or_data, dict):
            self.interface_data = path_or_data
        else:
            with open(path_or_data, 'r') as f:
                self.interface_data = json.load(f)
    
    def load_triage(self, path_or_data):
        """Load triage data from file or dict."""
        if isinstance(path_or_data, dict):
            self.triage_data = path_or_data
        else:
            with open(path_or_data, 'r') as f:
                self.triage_data = json.load(f)
    
    def generate(self, output_path: str) -> str:
        """Generate HTML report and save to file."""
        dispatch_table = self.interface_data.get('dispatch_table', [])
        ioctls = self.interface_data.get('ioctls', [])
        devices = self.interface_data.get('devices', [])
        
        clusters = self.triage_data.get('clusters', {})
        
        # Build dispatch rows
        dispatch_rows = ""
        for entry in dispatch_table:
            mj = entry.get('major_function', -1)
            mj_name = MAJOR_FUNCTION_NAMES.get(mj, f"IRP_MJ_{mj}")
            handler = entry.get('handler_name', 'unknown')
            addr = hex(entry.get('handler_ea', 0))
            irql = entry.get('irql', 'PASSIVE')
            dispatch_rows += f"<tr><td>{mj_name}</td><td class='code'>{handler}</td><td class='code'>{addr}</td><td>{irql}</td></tr>\n"
        
        if not dispatch_rows:
            dispatch_rows = "<tr><td colspan='4'>No dispatch handlers found</td></tr>"
        
        # Build IOCTL section
        if ioctls:
            ioctl_rows = ""
            for ioctl in ioctls:
                code = ioctl.get('code', 0)
                method = ioctl.get('method', 'UNKNOWN')
                badge_class = 'badge-buffered' if 'BUFFERED' in method else ('badge-direct' if 'DIRECT' in method else 'badge-neither')
                ioctl_rows += f"<tr><td class='code'>{hex(code)}</td><td><span class='badge {badge_class}'>{method}</span></td><td class='code'>{hex(ioctl.get('handler_ea', 0))}</td></tr>\n"
            ioctl_section = f"<table><tr><th>Code</th><th>Method</th><th>Handler</th></tr>{ioctl_rows}</table>"
        else:
            ioctl_section = "<p>No IOCTLs detected. Run analysis with full instruction dump enabled.</p>"
        
        # Build triage section
        if clusters:
            triage_rows = ""
            for sig, files in clusters.items():
                triage_rows += f"<tr><td class='code'>{sig[:50]}...</td><td>{len(files)}</td></tr>\n"
            triage_section = f"<table><tr><th>Signature</th><th>Count</th></tr>{triage_rows}</table>"
        else:
            triage_section = "<p>No crash data available. Run fuzzing and triage first.</p>"
        
        # Fill template
        html = HTML_TEMPLATE.format(
            timestamp=datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
            num_dispatch=len(dispatch_table),
            num_ioctls=len(ioctls),
            num_devices=len(devices),
            num_crashes=len(clusters),
            dispatch_rows=dispatch_rows,
            ioctl_section=ioctl_section,
            triage_section=triage_section
        )
        
        with open(output_path, 'w', encoding='utf-8') as f:
            f.write(html)
        
        return output_path
